let currentLang = localStorage.getItem('rbd-lang') || 'fr';

function t(key) {
  return (translations[currentLang] && translations[currentLang][key])
    || translations['fr'][key]
    || key;
}

function applyLang() {
  const isRTL = currentLang === 'darija' || currentLang === 'ar';
  document.documentElement.lang = currentLang === 'darija' ? 'ar-MA' : currentLang;
  document.documentElement.dir  = isRTL ? 'rtl' : 'ltr';

  /* Active button highlight */
  ['fr', 'en', 'ar', 'darija'].forEach(l => {
    const btn = document.getElementById('btn-lang-' + l);
    if (btn) btn.classList.toggle('active', l === currentLang);
  });

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('rbd-lang', lang);
  applyLang();
  if (typeof onLangChange === 'function') onLangChange();
}

document.addEventListener('DOMContentLoaded', applyLang);
